{
  //Cac phep toan
  int a = 5 + 7;
  int b = 7 - 2;
  int c = 5 * 6;
  double d = 10/2;
  int e = 7%3; // 1
  print(d);

  // Phep chia lay phan nguyen
  int f = 7 ~/2; // 1
